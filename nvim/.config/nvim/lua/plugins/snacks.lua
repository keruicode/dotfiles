return {
  {
    "folke/snacks.nvim",
    opts = {
      picker = {
        -- =======================================================
        -- 1. 全局列表通用按键 (影响如 Find Files, Grep 等普通搜索框)
        -- =======================================================
        win = {
          list = {
            keys = {
              ["e"] = "list_down",
              ["u"] = "list_up",
              ["n"] = "close", -- n (左): 在普通搜索框里直接关闭
              ["i"] = "confirm",
              ["/"] = "focus_input",
            },
          },
        },
        -- =======================================================
        -- 2. 专门针对 Explorer (文件树) 的最高优先级覆写
        -- =======================================================
        sources = {
          explorer = {
            win = {
              list = {
                keys = {
                  -- 必须在这里强行把 u 抢回来，否则会被默认的 "返回上一级" 覆盖
                  ["u"] = "list_up",
                  ["e"] = "list_down",

                  -- 针对文件树的专属左右操作
                  ["n"] = "explorer_close", -- n (左): 折叠当前目录 / 退回父节点
                  ["i"] = "confirm", -- i (右): 展开目录 / 打开文件

                  -- 如果你希望用其他的键（比如后退键或者 h）来返回上一级目录，可以额外加上：
                  -- ["h"] = "explorer_up",
                },
              },
            },
          },
        },
      },
    },
  },
}
